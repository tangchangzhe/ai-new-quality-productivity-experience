export function buildModelPrompt(content) {
  return `用户提出了一个关于"AI提升生产力"的方向。请你基于这个方向，提出你自己的具体构想——不是评价用户的想法，而是你来出一个更具体的落地方案或创新点子。

你可以：
- 提出一个用户没想到的切入角度
- 描述一个具体的技术实现路径
- 构想一个真实的应用场景和它带来的改变

用户的方向：
「${content}」

要求：直接输出你的构想，不要先复述或评价用户的想法。总字数控制在 150~250 字，语言简洁有力，不要用 Markdown 格式，不要用 bullet point，纯文本段落输出。`;
}

export const levelFramework = `第一层：工具替代 — AI 帮助人更快地完成已有工作，但工作方式和生产流程没有本质改变。例如用 AI 写周报、翻译文献、生成海报。

第二层：流程重构 — AI 重新定义了整条生产链的运行方式，不只是某个环节变快，而是环节之间的关系改变，甚至有些环节直接消失。例如特斯拉 FSD 用端到端神经网络替代传统分模块自动驾驶架构。

第三层：能力涌现 — AI 让人类做到以前根本不可能做到的事，不是加速而是从零到一。例如 AlphaFold 预测蛋白质结构、AI 独立推翻数学猜想。`;

export function buildEvaluationPrompt(content) {
  return `你是一位产业经济学评审专家。请根据以下三层框架评判一个用户提出的"AI 提升生产力"想法。

三层框架：
${levelFramework}

用户的想法：
「${content}」

请完成：
1. 判定这个想法属于哪一层（1/2/3）
2. 给出 1~100 的评分，评分标准：
   - 层级越高基础分越高（第一层 1~40，第二层 30~70，第三层 50~100）
   - 同层内看：想法的具体性、可落地性、是否触及真正的生产力瓶颈、创新程度
3. 用一句话点评这个想法（15~30字）

严格按以下 JSON 格式返回，不要输出任何其他内容：
{"level": 数字, "score": 数字, "comment": "一句话点评"}`;
}

export function buildResonancePrompt(content, historyIdeas) {
  const list = historyIdeas
    .map((idea, index) => `${index + 1}. 「${idea.content}」`)
    .join("\n");

  return `你是一个语义匹配助手。

下面是一个用户的想法：
「${content}」

下面是其他参与者提交过的想法列表（编号 1 到 N）：
${list}

请完成以下任务：
1. 找出与当前用户想法最相似的 2~3 条（相似指关注同一领域、同一问题、或同一技术路径），返回它们的编号
2. 用一个短语概括他们共同关注的方向（如"AI辅助医疗诊断""智能制造质检"等）

严格按以下 JSON 格式返回，不要输出任何其他内容：
{"direction": "共同方向短语", "similar_ids": [编号1, 编号2, 编号3], "total_same_direction": 数字}

其中 total_same_direction 是所有想法中（包括未被选中的）和该用户大致关注同一方向的总人数。`;
}
